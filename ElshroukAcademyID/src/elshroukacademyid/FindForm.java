    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
       
            try {
                 if (!txtID.getText().equals("")) {
                   BufferedReader reader=new BufferedReader(new InputStreamReader(new FileInputStream("StudentID.txt"), "UTF8"));
                   String line ="";
                   boolean found = false;
                     do {
                       line=reader.readLine();
                         if (line!=null) {
                             String[] arrLine = line.split(";");
                                 if (arrLine[0].equals(txtID.getText())) {
                                 txtName.setText(arrLine[1]);
                                 txtYear.setText(arrLine[2]);
                                 txtDept.setText(arrLine[3]);
                                 txtEmail.setText(arrLine[4]);
                                 found = true;
                                 break;
                             }
                            
                         }
                          
                     } while (line!=null);
                     reader.close();
                     
                     if (!found) {
                         JOptionPane.showMessageDialog(null,"This ID not found");
                         txtID.requestFocus();
                     }
                  }
                  else  {
                         JOptionPane.showMessageDialog(null,"Error");
                     }
            } catch (FileNotFoundException ex) {
                Logger.getLogger(FindForm.class.getName()).log(Level.SEVERE, null, ex);
            } catch (IOException ex) {
            Logger.getLogger(FindForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        
       

    }//GEN-LAST:event_btnSearchActionPerformed

